//  Variables

// using var (function-scoped and can be re-assigned)
var Firstname = "Jitendra";   
console.log(Firstname); /* output = jitendra */
Firstname = "aadi";  /*re-assign*/
console.log(Firstname);  /*output = aadi */


//using let (block-scoped and can be re-assigned)
let Lastname = "Patidar";
console.log(Lastname);  /* output=Patidar */
Lastname = "patel";  /* re-assign*/
console.log(Lastname);  /* output=patel*/

//using const (block-scoped and can't be re-assigned)
const City = 30;
console.log(City);


